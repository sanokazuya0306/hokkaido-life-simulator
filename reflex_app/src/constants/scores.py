# ============================================================================
# 生涯年収スコアの基準（パーセンタイル方式）
# 統計的スケール: 中央値60点、上位1%で100点、下位1%で0点
# 出典: 労働政策研究・研修機構「ユースフル労働統計」、厚生労働省「賃金構造基本統計調査」
