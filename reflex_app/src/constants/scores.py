def get_age_group_for_death_cause(age: int) -> str:
    """
    年齢から死因選択用の年代グループを取得
    
    Args:
        age: 死亡年齢
        
    Returns:
        年代グループ文字列（"0-9", "10-19", ... "90-99"）
    """
    if age < 10:
        return "0-9"
    elif age < 20:
        return "10-19"
    elif age < 30:
        return "20-29"
    elif age < 40:
        return "30-39"
    elif age < 50:
        return "40-49"
    elif age < 60:
        return "50-59"
    elif age < 70:
        return "60-69"
    elif age < 80:
        return "70-79"
    elif age < 90:
        return "80-89"
    else:
        return "90-99"
