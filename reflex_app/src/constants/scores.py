def _calculate_score_from_percentile(percentile: float) -> float:
    """
    パーセンタイルからスコアを計算（正規分布的なスコアリング）
    
    Args:
        percentile: パーセンタイル（0-100）
    
    Returns:
        スコア（0-100点）
    """
    if percentile < 50:
        score = percentile * 1.2
    else:
        score = 60 + (percentile - 50) * 0.8
    
    return max(0.0, min(100.0, score))


